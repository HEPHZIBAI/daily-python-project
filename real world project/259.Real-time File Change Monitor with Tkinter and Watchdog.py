'''
Project Description
    This project will monitor changes in a specified directory and display them in real-time using a simple GUI built with Tkinter. 
    You can use this to track modifications like file creation, deletion, or modification in any folder. 
    This could be useful for applications like monitoring log files or watching over a directory during development.

How the Project Works
    The program monitors a user-specified directory in real-time. 
    Whenever a file is added, removed, or modified in the directory, a log entry is displayed in a Tkinter GUI. 
    The GUI provides a real-time, scrolling log of all file changes, making it easier to see what is happening in the monitored folder.

Prerequisites
    Required Libraries: watchdog, tkinter
    Installation: pip install watchdog
'''